/**
 * Get executive moves from the last N days, sorted newest first.
 */
export function getRecentMoves(days: number): ExecutiveMove[] {
  const cutoff = new Date();
  cutoff.setDate(cutoff.getDate() - days);
  const cutoffStr = cutoff.toISOString().split('T')[0];

  return EXECUTIVE_MOVES
    .filter(m => m.date >= cutoffStr)
    .sort((a, b) => b.date.localeCompare(a.date));
}

/**
 * Get all moves involving a specific company (by slug or name).
 */
export function getMovesByCompany(slugOrName: string): ExecutiveMove[] {
  const lower = slugOrName.toLowerCase();
  return EXECUTIVE_MOVES.filter(m => {
    if (m.companySlug && m.companySlug.toLowerCase() === lower) return true;
    if (m.fromCompany && m.fromCompany.toLowerCase().includes(lower)) return true;
    if (m.toCompany && m.toCompany.toLowerCase().includes(lower)) return true;
    return false;
  }).sort((a, b) => b.date.localeCompare(a.date));
}

/**
 * Get the most notable moves (those with summaries, sorted newest first).
 */
export function getNotableMoves(): ExecutiveMove[] {
  return EXECUTIVE_MOVES
    .filter(m => m.summary && m.summary.length > 0)
    .sort((a, b) => b.date.localeCompare(a.date));
}
