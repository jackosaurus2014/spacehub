// ── ICS Generation Helper ──

export function generateICS(event: SpaceEvent): string {
  const formatDate = (dateStr: string) => dateStr.replace(/-/g, '');
  const start = formatDate(event.startDate) + 'T090000Z';
  const end = formatDate(event.endDate || event.startDate) + 'T180000Z';
  const now = new Date().toISOString().replace(/[-:]/g, '').split('.')[0] + 'Z';

  const escapeICS = (str: string) =>
    str.replace(/\\/g, '\\\\').replace(/;/g, '\\;').replace(/,/g, '\\,').replace(/\n/g, '\\n');

  return [
    'BEGIN:VCALENDAR',
    'VERSION:2.0',
    'PRODID:-//SpaceNexus//Space Events//EN',
    'CALSCALE:GREGORIAN',
    'METHOD:PUBLISH',
    'BEGIN:VEVENT',
    `DTSTART:${start}`,
    `DTEND:${end}`,
    `DTSTAMP:${now}`,
    `UID:${event.id}@spacenexus.us`,
    `SUMMARY:${escapeICS(event.name)}`,
    `DESCRIPTION:${escapeICS(event.description)}`,
    `LOCATION:${escapeICS(event.location)}${event.venue ? ' - ' + escapeICS(event.venue) : ''}`,
    `URL:${event.website}`,
    'END:VEVENT',
    'END:VCALENDAR',
  ].join('\r\n');
}

// ── Export all events as a combined ICS ──

export function generateMultiEventICS(events: SpaceEvent[]): string {
  const now = new Date().toISOString().replace(/[-:]/g, '').split('.')[0] + 'Z';
  const formatDate = (dateStr: string) => dateStr.replace(/-/g, '');

  const escapeICS = (str: string) =>
    str.replace(/\\/g, '\\\\').replace(/;/g, '\\;').replace(/,/g, '\\,').replace(/\n/g, '\\n');

  const vevents = events.map((event) => {
    const start = formatDate(event.startDate) + 'T090000Z';
    const end = formatDate(event.endDate || event.startDate) + 'T180000Z';
    return [
      'BEGIN:VEVENT',
      `DTSTART:${start}`,
      `DTEND:${end}`,
      `DTSTAMP:${now}`,
      `UID:${event.id}@spacenexus.us`,
      `SUMMARY:${escapeICS(event.name)}`,
      `DESCRIPTION:${escapeICS(event.description)}`,
      `LOCATION:${escapeICS(event.location)}${event.venue ? ' - ' + escapeICS(event.venue) : ''}`,
      `URL:${event.website}`,
      'END:VEVENT',
    ].join('\r\n');
  });

  return [
    'BEGIN:VCALENDAR',
    'VERSION:2.0',
    'PRODID:-//SpaceNexus//Space Events//EN',
    'CALSCALE:GREGORIAN',
    'METHOD:PUBLISH',
    'X-WR-CALNAME:SpaceNexus Space Events',
    ...vevents,
    'END:VCALENDAR',
  ].join('\r\n');
}

// ── Google Calendar link generator ──

export function generateGoogleCalendarUrl(event: SpaceEvent): string {
  const formatGoogleDate = (dateStr: string) => dateStr.replace(/-/g, '');
  const start = formatGoogleDate(event.startDate);
  const end = formatGoogleDate(event.endDate || event.startDate);
  // Google Calendar uses all-day event format: YYYYMMDD/YYYYMMDD (end is exclusive, add one day)
  const endDate = new Date(event.endDate || event.startDate);
  endDate.setDate(endDate.getDate() + 1);
  const endStr = endDate.toISOString().split('T')[0].replace(/-/g, '');

  const params = new URLSearchParams({
    action: 'TEMPLATE',
    text: event.name,
    dates: `${start}/${endStr}`,
    details: `${event.description}\n\n${event.website}`,
    location: event.venue ? `${event.venue}, ${event.location}` : event.location,
  });

  return `https://calendar.google.com/calendar/render?${params.toString()}`;
}

