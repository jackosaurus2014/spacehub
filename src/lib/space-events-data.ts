// ── Query helpers ──

export function getAllEvents(): SpaceEvent[] {
  return [...SPACE_EVENTS].sort((a, b) => a.startDate.localeCompare(b.startDate));
}

export function getUpcomingEvents(days: number = 90): SpaceEvent[] {
  const now = new Date();
  now.setHours(0, 0, 0, 0);
  const cutoff = new Date(now);
  cutoff.setDate(cutoff.getDate() + days);

  return SPACE_EVENTS
    .filter((e) => {
      const end = new Date((e.endDate || e.startDate) + 'T23:59:59');
      const start = new Date(e.startDate + 'T00:00:00');
      return end >= now && start <= cutoff;
    })
    .sort((a, b) => a.startDate.localeCompare(b.startDate));
}

export function getEventsByType(type: SpaceEvent['type']): SpaceEvent[] {
  return SPACE_EVENTS
    .filter((e) => e.type === type)
    .sort((a, b) => a.startDate.localeCompare(b.startDate));
}

export function getEventsByMonth(year: number, month: number): SpaceEvent[] {
  return SPACE_EVENTS
    .filter((e) => {
      const d = new Date(e.startDate + 'T00:00:00');
      return d.getFullYear() === year && d.getMonth() === month;
    })
    .sort((a, b) => a.startDate.localeCompare(b.startDate));
}

export function getEventCountries(): string[] {
  const countries = new Set<string>();
  SPACE_EVENTS.forEach((e) => countries.add(e.country));
  return Array.from(countries).sort();
}
