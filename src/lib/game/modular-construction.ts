// ─── Module Instance (tracked per station/colony) ────────────────────────────

export interface InstalledModule {
  moduleId: string;
  installedAtMs: number;
  slotIndex: number; // Which slot it occupies (0 to maxModuleSlots-1)
}

// ─── Station/Colony Instance ─────────────────────────────────────────────────

export interface ModularStructure {
  instanceId: string;
  hullId: string;
  locationId: string;
  name: string; // Player-customizable name
  isBuilt: boolean;
  buildStartedAtMs: number;
  buildDurationSeconds: number;
  modules: InstalledModule[];
  healthPct: number; // 0-100
  totalPowerGenerated: number;
  totalPowerConsumed: number;
}

// ─── Helpers ─────────────────────────────────────────────────────────────────

export function getHullDefinition(hullId: string): HullDefinition | undefined {
  return HULL_DEFINITIONS.find(h => h.id === hullId);
}

export function getModuleDefinition(moduleId: string): ModuleDefinition | undefined {
  return MODULE_DEFINITIONS.find(m => m.id === moduleId);
}

export function getAvailableSlots(structure: ModularStructure): number {
  const hull = getHullDefinition(structure.hullId);
  if (!hull) return 0;
  return hull.maxModuleSlots - structure.modules.length;
}

export function calculateStationBonuses(structure: ModularStructure): {
  totalPopulation: number;
  totalRevenue: number;
  totalMining: number;
  totalResearch: number;
  totalShipCapacity: number;
  totalDefense: number;
  totalStorage: number;
  totalPower: number;
  powerBalance: number;
  maintenanceReduction: number;
} {
  const hull = getHullDefinition(structure.hullId);
  if (!hull) return { totalPopulation: 0, totalRevenue: 0, totalMining: 0, totalResearch: 0, totalShipCapacity: 0, totalDefense: 0, totalStorage: 0, totalPower: 0, powerBalance: 0, maintenanceReduction: 0 };

  let pop = hull.basePopulation;
  let rev = 0, mine = 0, res = 0, ships = 0, def = hull.baseDefense, storage = 0;
  let powerGen = hull.basePower, powerUse = 0, maintRed = 0;

  for (const installed of structure.modules) {
    const mod = getModuleDefinition(installed.moduleId);
    if (!mod) continue;
    if (mod.bonuses.populationCapacity) pop += mod.bonuses.populationCapacity;
    if (mod.bonuses.revenueBoost) rev += mod.bonuses.revenueBoost;
    if (mod.bonuses.miningBoost) mine += mod.bonuses.miningBoost;
    if (mod.bonuses.researchSpeed) res += mod.bonuses.researchSpeed;
    if (mod.bonuses.shipCapacity) ships += mod.bonuses.shipCapacity;
    if (mod.bonuses.defenseRating) def += mod.bonuses.defenseRating;
    if (mod.bonuses.storageCapacity) storage += mod.bonuses.storageCapacity;
    if (mod.bonuses.powerGeneration) powerGen += mod.bonuses.powerGeneration;
    if (mod.bonuses.maintenanceReduction) maintRed += mod.bonuses.maintenanceReduction;
    powerUse += mod.powerCost;
  }

  return {
    totalPopulation: pop,
    totalRevenue: rev,
    totalMining: mine,
    totalResearch: res,
    totalShipCapacity: ships,
    totalDefense: def,
    totalStorage: storage,
    totalPower: powerGen,
    powerBalance: powerGen - powerUse,
    maintenanceReduction: Math.min(0.50, maintRed), // Cap at 50%
  };
}

/** Check if a module can be installed (has power, has slot, etc.) */
export function canInstallModule(
  structure: ModularStructure,
  moduleId: string,
): { canInstall: boolean; reason?: string } {
  const hull = getHullDefinition(structure.hullId);
  const mod = getModuleDefinition(moduleId);
  if (!hull) return { canInstall: false, reason: 'Invalid hull' };
  if (!mod) return { canInstall: false, reason: 'Invalid module' };

  // Check slot availability
  if (structure.modules.length >= hull.maxModuleSlots) {
    return { canInstall: false, reason: `No module slots available (${hull.maxModuleSlots}/${hull.maxModuleSlots} used)` };
  }

  // Check power balance
  const bonuses = calculateStationBonuses(structure);
  if (bonuses.powerBalance < mod.powerCost) {
    return { canInstall: false, reason: `Insufficient power (need ${mod.powerCost}, have ${bonuses.powerBalance} available). Install a power module first.` };
  }

  return { canInstall: true };
}

