// ─── Space Tycoon: Modular Construction System ──────────────────────────────
//
// Starbases, space stations, and planetary colonies are no longer monolithic
// buildings. They start as a core hull and are expanded by attaching MODULES.
//
// Each module is a crafted production-chain item (station_module, habitat_pod,
// etc.) that provides specific capabilities when installed.
//
// DESIGN:
// 1. Build core structure (hull) — provides basic functionality
// 2. Attach modules to add capabilities (housing, power, labs, docks)
// 3. Each module type provides different bonuses
// 4. Module slots are limited by hull size (small=4, medium=8, large=16)
// 5. Modules require crafted components — creates demand for production chains
// 6. Modules can be swapped out (lost module, not destroyed)
// 7. Different module combos create different station "specializations"
//
// This integrates with the production chain system — station_module and
// habitat_pod are already craftable items. Now they have a USE.

// ─── Module Definitions ──────────────────────────────────────────────────────

export interface ModuleDefinition {
  id: string;
  name: string;
  icon: string;
  description: string;
  category: ModuleCategory;
  tier: number;
  // What crafted item is consumed to install this module
  requiredItem: string; // ID from production-chains.ts (e.g. 'station_module')
  requiredItemQuantity: number;
  // Additional resource costs for installation
  installCost: number; // Money
  installResources: Record<string, number>;
  installTimeSeconds: number; // Real-time seconds to install
  // What this module provides
  bonuses: {
    populationCapacity?: number;   // How many people this module houses
    revenueBoost?: number;         // +X% revenue for the station/colony
    miningBoost?: number;          // +X% mining output at this location
    researchSpeed?: number;        // +X% research speed
    shipCapacity?: number;         // +X ship docking slots
    defenseRating?: number;        // +X defense against contests
    storageCapacity?: number;      // +X resource storage slots
    powerGeneration?: number;      // +X power units (some modules need power)
    maintenanceReduction?: number; // -X% maintenance cost
  };
  powerCost: number; // Power units consumed (0 = generates power)
  maintenancePerMonth: number; // Monthly upkeep
}

export type ModuleCategory =
  | 'habitation'    // Living quarters, life support
  | 'power'         // Solar arrays, reactors
  | 'science'       // Labs, observatories
  | 'industry'      // Fabrication, refining
  | 'defense'       // Shields, weapons, armor
  | 'docking'       // Ship berths, cargo handling
  | 'storage'       // Resource tanks, warehouses
  | 'communications' // Relay arrays, data centers
  | 'recreation'    // Tourism, morale
  | 'command';      // Bridge, control center

